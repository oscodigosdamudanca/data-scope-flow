// TESTES DE ESTRUTURA DAS TABELAS
// =====================================================

async function testTableStructure(logger) {
  logger.log('ESTRUTURA', 'In√≠cio dos Testes', 'INFO', 'Verificando estrutura das tabelas...');
  
  const tables = [
    'profiles',
    'companies', 
    'company_memberships',
    'user_roles',
    'leads',
    'surveys',
    'survey_questions',
    'survey_responses'
  ];
  
  let accessibleTables = 0;
  
  for (const table of tables) {
    try {
      const { data, error } = await supabase
        .from(table)
        .select('*')
        .limit(1);
      
      if (error) {
        logger.log('ESTRUTURA', `Tabela ${table}`, 'FAIL', `Erro ao acessar: ${error.message}`);
      } else {
        logger.log('ESTRUTURA', `Tabela ${table}`, 'PASS', 'Tabela acess√≠vel');
        accessibleTables++;
      }
    } catch (error) {
      logger.log('ESTRUTURA', `Tabela ${table}`, 'FAIL', `Exce√ß√£o: ${error.message}`);
    }
  }
  
  const successRate = ((accessibleTables / tables.length) * 100).toFixed(1);
  logger.log('ESTRUTURA', 'Resumo Estrutura', 'INFO', `${accessibleTables}/${tables.length} tabelas acess√≠veis (${successRate}%)`);
  
  return accessibleTables > 0;
}

