// TESTES DE POL√çTICAS RLS
// =====================================================

async function testRLSPolicies(logger) {
  logger.log('RLS', 'In√≠cio dos Testes', 'INFO', 'Verificando pol√≠ticas RLS...');
  
  // Teste de acesso sem autentica√ß√£o
  const tables = ['profiles', 'companies', 'leads', 'surveys'];
  
  for (const table of tables) {
    try {
      const { data, error } = await supabase
        .from(table)
        .select('*')
        .limit(1);
      
      if (error && error.message.includes('row-level security')) {
        logger.log('RLS', `Pol√≠tica ${table}`, 'PASS', 'RLS ativo - acesso negado sem autentica√ß√£o');
      } else if (error) {
        logger.log('RLS', `Pol√≠tica ${table}`, 'FAIL', `Erro: ${error.message}`);
      } else {
        logger.log('RLS', `Pol√≠tica ${table}`, 'INFO', 'Acesso permitido - RLS pode estar desabilitado');
      }
    } catch (error) {
      logger.log('RLS', `Pol√≠tica ${table}`, 'FAIL', `Exce√ß√£o: ${error.message}`);
    }
  }
  
  return true;
}

