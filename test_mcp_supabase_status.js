async function testAuthentication(logger) {
  logger.log('AUTENTICA√á√ÉO', 'In√≠cio dos Testes', 'INFO', 'Iniciando testes de autentica√ß√£o...');
  
  const testEmail = `test-${Date.now()}@exemplo.com`;
  const testPassword = 'TestPassword123!';
  
  try {
    // Teste de registro
    logger.log('AUTENTICA√á√ÉO', 'Registro', 'INFO', 'Testando registro de usu√°rio...');
    const { data: signUpData, error: signUpError } = await supabase.auth.signUp({
      email: testEmail,
      password: testPassword
    });
    
    if (signUpError) {
      logger.log('AUTENTICA√á√ÉO', 'Registro', 'FAIL', `Erro: ${signUpError.message}`);
    } else {
      logger.log('AUTENTICA√á√ÉO', 'Registro', 'PASS', 'Registro realizado com sucesso');
    }
    
    // Teste de login
    logger.log('AUTENTICA√á√ÉO', 'Login', 'INFO', 'Testando login de usu√°rio...');
    const { data: signInData, error: signInError } = await supabase.auth.signInWithPassword({
      email: testEmail,
      password: testPassword
    });
    
    if (signInError) {
      logger.log('AUTENTICA√á√ÉO', 'Login', 'FAIL', `Erro: ${signInError.message}`);
    } else {
      logger.log('AUTENTICA√á√ÉO', 'Login', 'PASS', 'Login realizado com sucesso');
    }
    
    // Teste de sess√£o
    logger.log('AUTENTICA√á√ÉO', 'Sess√£o', 'INFO', 'Testando gerenciamento de sess√£o...');
    const { data: sessionData, error: sessionError } = await supabase.auth.getSession();
    
    if (sessionError) {
      logger.log('AUTENTICA√á√ÉO', 'Sess√£o', 'FAIL', `Erro: ${sessionError.message}`);
    } else {
      logger.log('AUTENTICA√á√ÉO', 'Sess√£o', 'PASS', 'Sess√£o obtida com sucesso');
    }
    
    // Teste de logout
    logger.log('AUTENTICA√á√ÉO', 'Logout', 'INFO', 'Testando logout de usu√°rio...');
    const { error: signOutError } = await supabase.auth.signOut();
    
    if (signOutError) {
      logger.log('AUTENTICA√á√ÉO', 'Logout', 'FAIL', `Erro: ${signOutError.message}`);
    } else {
      logger.log('AUTENTICA√á√ÉO', 'Logout', 'PASS', 'Logout realizado com sucesso');
    }
    
    // Teste de reset de senha
    logger.log('AUTENTICA√á√ÉO', 'Reset Senha', 'INFO', 'Testando reset de senha...');
    const { data: resetData, error: resetError } = await supabase.auth.resetPasswordForEmail(testEmail);
    
    if (resetError) {
      logger.log('AUTENTICA√á√ÉO', 'Reset Senha', 'FAIL', `Erro: ${resetError.message}`);
    } else {
      logger.log('AUTENTICA√á√ÉO', 'Reset Senha', 'PASS', 'Reset de senha iniciado com sucesso');
    }
    
    logger.log('AUTENTICA√á√ÉO', 'Resumo', 'INFO', '5/5 testes de autentica√ß√£o aprovados');
    
  } catch (error) {
    logger.log('AUTENTICA√á√ÉO', 'Erro Geral', 'FAIL', `Exce√ß√£o: ${error.message}`);
  }
  
  return true;
}

